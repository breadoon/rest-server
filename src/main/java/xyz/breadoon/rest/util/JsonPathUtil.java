package xyz.breadoon.rest.util;

import com.fasterxml.jackson.core.JsonProcessingException;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.jayway.jsonpath.Configuration;
import com.jayway.jsonpath.DocumentContext;
import com.jayway.jsonpath.JsonPath;
import com.jayway.jsonpath.spi.json.JacksonJsonProvider;
import com.jayway.jsonpath.spi.mapper.JacksonMappingProvider;

public class JsonPathUtil {

	private static final Configuration config = Configuration.defaultConfiguration()
            .jsonProvider(new JacksonJsonProvider())
            .mappingProvider(new JacksonMappingProvider());
	private static final ObjectMapper mapper = new ObjectMapper();
	
	public static DocumentContext getDocumentContext(Object obj) throws JsonProcessingException {
		return JsonPath.using(config).parse(mapper.writerWithDefaultPrettyPrinter().writeValueAsString(obj));
	}
}
